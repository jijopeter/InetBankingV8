package superKeywordDemo;

public class ParentClass 
{
	
	public ParentClass()
	{
		System.out.println("i am in parent class cons ");
	}
	public ParentClass(String name)
	{
		System.out.println("i am in parent class cons "+name);	
	}
	public ParentClass(String fname,String lname)
	{
		System.out.println("i am in parent class cons "+fname + " "+lname);	
	}
}
