package conditionalStatement;

public class switcheg3 {

	public static void main(String[] args) {
		int i=6;
		switch(i)
		{
		case 1:
			System.out.println("english");
			break;
		case 2:
			System.out.println("maths");
			break;
		case 3:
			System.out.println("hindi");
			break;
			default:
				System.out.println("other subject");
		}

	}

}
