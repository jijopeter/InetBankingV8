package conditionalStatement;

public class switcheg1 {

	public static void main(String[] args) {
		int x=2;
		switch(x)
		{
		case 1:
			System.out.println("new joining");
			break;
		case 2:
			System.out.println("experienced");
			break;
		case 3:
			System.out.println("expert");
			break;
			default:
				System.out.println("outsider");
				
		}
				

	}

}
