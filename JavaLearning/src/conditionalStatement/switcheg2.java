package conditionalStatement;

public class switcheg2 {

	public static void main(String[] args) {
		int a=5;
		switch(a)
		{
		case 1:
				System.out.println("training1");
				break;
		case 2:
			System.out.println("training2");
			break;
	    case 3:
	    	System.out.println("training3");
	    	break;
	    case 4:
	    	System.out.println("training4");
	    	break;
	    case 5:
	    	System.out.println("training5");
	    	break;
	    	default:
	    	System.out.println("finished training");
		}
}
}
