package conditionalpractice;

public class switch1 {

	public static void main(String[] args) {
		int roll_number=5;
		switch(roll_number)
		{
		case 1:
			System.out.println("outsider");
			break;
		case 2:
			System.out.println("old student");
			break;
		case 3:
			System.out.println("school student");
			break;
		case 4 :
			System.out.println("existing student");
			break;
		default:
			System.out.println("new joining");
			
		}
		
			

	}

}
